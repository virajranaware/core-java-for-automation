package org.example.week5;

public class ArithmaticOperations {

    int add(int n1, int n2){
        return n1 + n2;
    }

    double add(
            double n1,
            double n2){
        return n1 + n2;
    }

    double add(double n1, double n2, double n3){
        return n1 + n2 + n3;
    }


}
