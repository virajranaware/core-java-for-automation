package org.example.week2;

public class MainClass2 {

    public static void main(String[] args) {

        int a = 10;

        int b = 11;

        b += a;   // (b = b + a)
        System.out.println("Value of b : " + b);

        b *= a; // b = b * a
        System.out.println("value of b : " + b);

        b -= a; // b = b - a
        System.out.println("value of b : " + b);

    }
}
